// BASIC SYNTAX OF A WHILE LOOP
while (condition) {
  // Code to execute as long as the condition is true
}

// EXAMPLE COUNTING UP
let count = 3;
while (count > 3){
    console.log('Count is: ' + count)
}

// BASIC SYNTAX OF A FOR LOOP
for (initialization; condition; increment) {
  // Code to execute as long as the condition is true
}

// EXAMPLE COUNTING UP
for (let i = 0; i < 5; i++) {
    console.log('i is: ' + i);
}

// EXAMPLE COUNTING DOWN
for (let j = 5; j > 0; j--) {
    console.log('j is: ' + j);
}

